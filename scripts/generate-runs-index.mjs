#!/usr/bin/env node
// Scans src/data/runs/*.json and writes src/data/runs/_index.ts so the site
// auto-discovers any run JSON the agent-runner emits. Run before `next build`.

import { readdirSync, writeFileSync } from "node:fs";
import { join } from "node:path";

const RUNS_DIR = join(process.cwd(), "src", "data", "runs");
const OUT = join(RUNS_DIR, "_index.ts");

const files = readdirSync(RUNS_DIR)
  .filter((f) => f.endsWith(".json") && !f.startsWith("_"))
  .sort();

const imports = files
  .map((f, i) => `import r${i} from "./${f}";`)
  .join("\n");
const list = files.map((_, i) => `r${i} as Run`).join(",\n  ");

const body = `// AUTO-GENERATED by scripts/generate-runs-index.mjs. Do not edit by hand.
import type { Run } from "@/lib/types";
${imports}

export const ALL_RUNS: Run[] = [
  ${list}
];
`;

writeFileSync(OUT, body);
console.log(`generated ${OUT} with ${files.length} runs`);
